
public class OperatorPractice {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 15;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(b/a); //interger division
		System.out.println(b%a);
		
		int c = 42;
		System.out.println(c++); //wrong 
		System.out.println(++c); //right

		int d = 42;
		d--;
		
		int e = 10;
		int f = 10;
		
		System.out.println(e == f); //do a check for equality
		System.out.println(e!=f); //not equal
		
		boolean h = true;
		boolean i = true;
		System.out.println(h && i); //logical AND
		System.out.println(h || i); //logical OR
		
		// Jessica is 23 years old.
		int jessicaAge = 23;

		// Sam is 47.
		int samAge = 47;

		// Jessica is younger than Sam.
		System.out.println(jessicaAge < samAge);
		
		
		
}}

